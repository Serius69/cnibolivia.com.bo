                                            <p style="text-align: justify">
                                                La Cámara Nacional de Industrias se fundó en la ciudad de La Paz, un 28 de febrero de 1931 como Cámara de Fomento Industrial que agrupaba y representaba a los empresarios privados dedicados a la manufactura. <br><br>

En 1938, se reunió el Primer Congreso Nacional de Industriales y por mandato del mismo, la  Cámara de Fomento Industrial tomó el nombre de Cámara Nacional de Industrias. <br><br>

Ese mismo año se creó el Consejo Industrial Nacional, conformado por representantes del gobierno central, la Aduana Nacional, la Sociedad de Agricultura, Ganadería y Fomento Triguero y la Cámara Nacional de Industrias.<br><br>

En 1935, la CNI comenzó a realizar importantes aportes a la sociedad, como trabajar en estrecha relación con el Gobierno en la elaboración de la Ley General del Trabajo. <br><br>

En 1941 la CNI fue parte del Directorio de la Caja de Seguro y Ahorro Obrero, en representación de los industriales, quienes aportaron significativamente a esa Entidad. <br><br>

En 1944 la CNI fundó en La Paz la Escuela Industrial de la Nación Pedro Domingo Murillo, instituto que capacitaba técnicamente a los recursos humanos que eran requeridos por la industria. <br><br>

En 1945 la Cámara Nacional de Industrias concluyó los estudios definitivos para construir el Hospital Obrero N° 1 en la ciudad de La Paz, que continúa hasta el presente, como el más importante Hospital del sistema de salud, siendo ésta una de las más importantes contribuciones sociales que hace durante su vida institucional, en defensa del bienestar de los trabajadores del sector fabril  paceño. <br><br>

El 10 de mayo de 1945 formuló  el Proyecto de Ley de Fomento Industrial y lo sometió a consideración del Gobierno. Esta norma es uno de los documentos gremiales más importantes en la concepción del desarrollo productivo en general y visualiza mecanismos de formación profesional, instrumentos de promoción industrial, protección de los recursos humanos; además de asignar roles y competencias a instituciones relacionadas con el desarrollo. <br><br>

Entre 1946 y 1949, participó activamente en la promulgación de tratados comerciales con los países vecinos de Perú y Argentina. <br><br>

En 1948 organizó la primera Gran Feria Internacional en la ciudad de La Paz, ocasión en la que  el país y otras naciones mostraron su producción, iniciando un intercambio cada vez más amplio de productos. <br><br>

A principios de la década de los 60, participó apoyando al gobierno nacional en la suscripción y conformación de la Asociación Latinoamericana de Libre Comercio (ALALC) y también en la firma del Acuerdo de Cartagena, hoy Comunidad Andina de Naciones. <br><br>

También, a principios de la misma década, promovió y fundó el banco de fomento al desarrollo, Banco Industrial, el cual, luego de haber cumplido los objetivos para los que fue creado, pasó a ser un banco comercial, hoy Banco BISA. <br><br>

Promovió y presidió en 1972  la reunión de la Asociación de Industriales Latinoamericanos en la ciudad de La Paz. <br><br>

Durante la década de los años 80,  y, ante la apertura y cambio de modelo económico, gestionó la conformación del Instituto de Normalización y Calidad (IBNORCA), que hoy por hoy es un importante instrumento para garantizar la calidad de las mercancías que son consumidas por los ciudadanos. <br><br>

En la década de los 90, impulsó  la competitividad, logrando a fines del siglo XX la conformación de los Consejos de Calidad, de Competitividad y de Desarrollo Sostenible. <br><br>

En los inicios del Siglo XXI, su compromiso con el medio ambiente es claro y definitivo y muestra de ello es su contribución a la normativa que reglamenta la Ley del Medio Ambiente, con el Reglamento Ambiental para la Industria Manufacturera (RASIM) y la creación y consolidación del Centro de Promoción de Tecnologías Sostenibles (CPTS). <br><br>

El 2004, la CNI presentó a los empresarios industriales una nueva visión de la Responsabilidad Social Empresarial, que se refiere a un conjunto de valores que implican superar la mera preocupación del empresario por los resultados económicos de la gestión y proyectar a la empresa hacia su sociedad. Es una conducta definida en cuatro grandes ámbitos: la responsabilidad con los clientes, la responsabilidad con los trabajadores, la responsabilidad con las futuras generaciones (Medio Ambiente) y la responsabilidad con el conjunto de la sociedad que la cobija. <br><br>


